    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        String pName;
        String pStock;
        
            pName = productName.getText();
            pStock = productStock.getText();
            
        try {
            pst = con.prepareStatement("INSERT INTO product (product_name,product_stock) VALUES (?,?)");
            pst.setString(1, pName);
            pst.setString(2, pStock);
            
            pst.executeUpdate();
            
            productName.setText("");
            productStock.setText("");
            
            JOptionPane.showMessageDialog(null, "Record been added.");
            
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

